    private void JTableBuscarClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JTableBuscarClienteMouseClicked
        // TODO add your handling code here:
        int fila = JTableBuscarCliente.getSelectedRow();
        bus = JTableBuscarCliente.getValueAt(fila, 0).toString();
        opcionClick = true;
        try {

            Connection cn = Conexion.conectar();
            PreparedStatement ps = cn.prepareStatement("SELECT id_cliente, nombre_cliente FROM cliente WHERE id_cliente='" + bus + "'");
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                if(ventaap == true){
                    JTextNombreClienteForm.setText(rs.getString("nombre_cliente"));
                }else if(apartado == true){
                    codigoCli = rs.getString("id_cliente");
                    JTextNombreClienteAAForm.setText(rs.getString("nombre_cliente"));
                } else if(abono == true){
                    JTFCodigoClientesAbForm.setText(rs.getString("id_cliente"));
                    codigoClienteAB = rs.getString("id_cliente");
                    JTFNombreClientesAbForm.setText(rs.getString("nombre_cliente"));
                }
                
            }
        } catch (SQLException ex) {
            System.out.println(ex.toString());
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(RegistrarProducto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_JTableBuscarClienteMouseClicked

