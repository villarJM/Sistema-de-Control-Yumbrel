    private void CargarDatosTabla() {
        try {
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.addColumn("Codigo");
            modelo.addColumn("Nombre Completo");
            JTableBuscarCliente.setModel(modelo);

            Connection cn = Conexion.conectar();
            Statement st = cn.createStatement();

            String sql = "SELECT id_cliente, nombre_cliente FROM cliente;";

            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                Object[] filas = new Object[2];
                for (int i = 0; i < 2; i++) {
                    filas[i] = rs.getObject(i + 1);
                }
                modelo.addRow(filas);
            }

        } catch (SQLException ex) {
            System.err.println(ex.toString());
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(RegistrarProducto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void BusquedaTiempoReal() {
        try {
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.addColumn("Codigo");
            modelo.addColumn("Descripcion");
            JTableBuscarCliente.setModel(modelo);

            Connection cn = Conexion.conectar();
            Statement st = cn.createStatement();

            String sql = "SELECT id_cliente, nombre_cliente FROM cliente WHERE "
                    + "id_cliente LIKE '%" + JTFOrdenarPorClForm.getText().trim() + "%' "
                    + "OR "
                    + "nombre_cliente LIKE '%" + JTFOrdenarPorClForm.getText().trim() + "%';";
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                Object[] filas = new Object[2];
                for (int i = 0; i < 2; i++) {
                    filas[i] = rs.getObject(i + 1);
                }
                modelo.addRow(filas);
            }

        } catch (SQLException ex) {
            System.err.println(ex.toString());
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(RegistrarProducto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
