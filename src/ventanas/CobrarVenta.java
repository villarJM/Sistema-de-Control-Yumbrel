    private void JBTerminarVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBTerminarVentaActionPerformed
        // TODO add your handling code here:
        JLProductoAPagar.setText(String.valueOf(totalventa));
        float cambio = 0;
        float importe = Float.parseFloat(JTextFEntradaPago.getText().trim());
        if (importe <= 0 || totalventa <= 0) {
            JOptionPane.showMessageDialog(null, "Operacion Invalida!!");
        } else {
            JLProductoAPaga.setText(String.valueOf(importe));
            if (cambio <= 0) {
                cambio = (totalventa - importe) * -1;
                JLProCambio.setText(String.valueOf(cambio));
                JLResultadoCambioVen.setText(String.valueOf(cambio));
            } else {
                cambio = totalventa - importe;
                JLProCambio.setText(String.valueOf(cambio));
                JLResultadoCambioVen.setText(String.valueOf(cambio));
            }

        }

    }//GEN-LAST:event_JBTerminarVentaActionPerformed

