    private void JTableAreaClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JTableAreaClientesMouseClicked
        // TODO add your handling code here:
        int fila = JTableAreaClientes.getSelectedRow();
        bus = String.valueOf(JTableAreaClientes.getValueAt(fila, 0));
        opcionClick = true;
        try {

            Connection cn = Conexion.conectar();

            PreparedStatement ps = cn.prepareStatement("SELECT * FROM cliente WHERE id_cliente='" + bus + "'");
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                JTextCodigoClienteForm.setText(rs.getString("id_cliente"));
                JTextNombreForm.setText(rs.getString("nombre_cliente"));
                JTextDireccionForm.setText(rs.getString("direccion_cliente"));
                JTextTelefonoForm.setText(rs.getString("telefono_cliente"));
            }
        } catch (SQLException ex) {
            System.out.println(ex.toString());
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(RegistrarProducto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_JTableAreaClientesMouseClicked

