    private void JButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JButtonGuardarActionPerformed
        // TODO add your handling code here:

        if ("".equals(JTextCodigoClienteForm.getText().trim()) || "".equals(JTextNombreForm.getText().trim()) || "".equals(JTextDireccionForm.getText().trim()) || "".equals(JTextTelefonoForm.getText().trim())) {
            JTextCodigoClienteForm.setText("Campo Obligatorio!!!");
            JTextCodigoClienteForm.setForeground(Color.red);
            JTextNombreForm.setText("Campo Obligatorio!!!");
            JTextNombreForm.setForeground(Color.red);
            JTextDireccionForm.setText("Campo Obligatorio!!!");
            JTextDireccionForm.setForeground(Color.red);
            JTextTelefonoForm.setText("Campo Obligatorio!!!");
            JTextTelefonoForm.setForeground(Color.red);
        } else if (opcionClick == true) {
            try {
                String sql = "UPDATE cliente\n"
                        + "SET nombre_cliente='" + JTextNombreForm.getText().trim() + "',"
                        + "direccion_cliente='" + JTextDireccionForm.getText().trim() + "',"
                        + "telefono_cliente=" + JTextTelefonoForm.getText().trim() + "\n"
                        + "WHERE id_cliente=" + bus;
                Connection cn = Conexion.conectar();
                PreparedStatement ps = cn.prepareStatement(sql);
                ps.execute();
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(RegistrarProducto.class.getName()).log(Level.SEVERE, null, ex);
            }
            CargarDatosTabla();
            JOptionPane.showMessageDialog(null, "Registro Actualizado Correctamente");
            opcionClick = false;
        } else {
            String codigo, nombre, direccion;
            long telefono;
            codigo = JTextCodigoClienteForm.getText().trim();
            nombre = JTextNombreForm.getText().trim();
            direccion = JTextDireccionForm.getText().trim();
            telefono = Long.parseLong(JTextTelefonoForm.getText().trim());

            if (JTextTelefonoForm.getText().trim().length() == 10) {
                try {
                    System.out.println("" + JTextTelefonoForm.getText().trim().length());
                    //Realiza la conexion a la Base de Datos desde la clase Conexion
                    Connection cn = Conexion.conectar();
                    Statement st = cn.createStatement();
                    st.addBatch("INSERT INTO cliente (id_cliente, nombre_cliente, direccion_cliente, telefono_cliente)"
                            + "VALUES ('" + codigo + "','" + nombre + "','" + direccion + "'," + telefono + ");");
                    st.executeBatch();

                    DefaultTableModel modelo = new DefaultTableModel();
                    JTableAreaClientes.setModel(modelo);
                    Object[] Nuevafila = {codigo, nombre, direccion, telefono};
                    modelo.addRow(Nuevafila);

                    CargarDatosTabla();

                } catch (SQLException e) {
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(RegistrarProducto.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JTextTelefonoForm.setText("Entrada Invalida!!");
            }
        }
    }//GEN-LAST:event_JButtonGuardarActionPerformed

